package com.gsom.ui.image_ui;

import java.util.ArrayList;

/**
 *
 * @author Thush
 */
public class SingleNodeImageModel {
    
    private String hitNode;
    private ArrayList<String> imgList;

    /**
     * @return the hitNode
     */
    public String getHitNode() {
        return hitNode;
    }

    /**
     * @param hitNode the hitNode to set
     */
    public void setHitNode(String hitNode) {
        this.hitNode = hitNode;
    }

    /**
     * @return the imgList
     */
    public ArrayList<String> getImgList() {
        return imgList;
    }

    /**
     * @param imgList the imgList to set
     */
    public void setImgList(ArrayList<String> imgList) {
        this.imgList = imgList;
    }
    
    
}
